'use strict'
let x = 7;
console.log(x = x % 3); 
// The remainder is going to be 1 ( 7 - 3 * 2 = 1 );
// Because 3 can be substracted two times from 7 
// before there is not enough left to substract again.

let y = 21;
console.log(y = y % 4);
// The remainder is going to be 1 ( 21 - 4 * 5 = 1);
// Because 4 can be substracted five times from 21 
// before there is not enough left to substract again.

let z = 13;
console.log(z = z % 2);
// The remainder is going to be 1 ( 13 - 2 * 6 = 1);
// Because 2 can be substracted six times from 13 
// before there is not enough left to substract again.